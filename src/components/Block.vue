<template>
    <div @click="stopTimer" v-if="showBlock" class="block">
        click me
    </div>
</template>

<script>
export default {
    props: ["delay"],
    data: () => ({
        showBlock: false,
        timer: null,
        reactionTime: 0
    }),
    mounted() {
        setTimeout(() => {
            this.showBlock = true;
            this.startTimer();
        }, this.delay);
    },
    methods: {
        startTimer() {
            this.timer = setInterval(() => {
                this.reactionTime += 10;
            }, 10);
        },
        stopTimer() {
            clearInterval(this.timer);
            this.$emit("end", this.reactionTime);
        }
    }
}
</script>

<style scoped>
.block {
    width: 400px;
    border-radius: 20px;
    background-color: #0faf87;
    color: white;
    text-align: center;
    padding: 100px 0;
    margin: 40px auto;
}
